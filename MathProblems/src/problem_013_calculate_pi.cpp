/**
 * ЗАДАЧА 13: ВЫЧИСЛЕНИЕ ЗНАЧЕНИЯ ЧИСЛА π
 * ========================================
 *
 * Вычисляет приближённое значение числа π с помощью ряда Лейбница
 * (также известного как ряд Грегори-Лейбница).
 *
 * ----------------------------------------------------------------------------
 * 1. МАТЕМАТИЧЕСКОЕ ОБОСНОВАНИЕ
 * ----------------------------------------------------------------------------
 *
 * Ряд Лейбница является частным случаем разложения арктангенса в ряд Тейлора:
 *
 *   arctan(x) = x - x³/3 + x⁵/5 - x⁷/7 + x⁹/9 - ...   (|x| ≤ 1)
 *
 * Подставляя x = 1, получаем:
 *
 *   arctan(1) = 1 - 1/3 + 1/5 - 1/7 + 1/9 - ...
 *
 * Так как arctan(1) = π/4, то:
 *
 *   π = 4 * (1 - 1/3 + 1/5 - 1/7 + 1/9 - ...)
 *
 * Это и есть ряд Лейбница для π.
 *
 * СВОЙСТВА РЯДА:
 *   • Знакочередующийся (члены чередуются по знаку).
 *   • Члены монотонно убывают по модулю и стремятся к нулю.
 *   • По признаку Лейбница ряд сходится.
 *   • Ошибка частичной суммы не превышает модуля первого отброшенного члена.
 *
 * ----------------------------------------------------------------------------
 * 2. АЛГОРИТМ
 * ----------------------------------------------------------------------------
 *
 *   1. Инициализируем сумму `sum = 0.0`.
 *   2. Для каждого i от 0 до iterations-1:
 *        - вычисляем член ряда: 1.0 / (2*i + 1)
 *        - прибавляем его с учётом знака: знак чередуется (+, -, +, -...)
 *   3. Умножаем полученную сумму на 4.
 *
 * ----------------------------------------------------------------------------
 * 3. ТОЧНОСТЬ
 * ----------------------------------------------------------------------------
 *
 * Ряд Лейбница сходится медленно. Ошибка частичной суммы не превышает 4/(2N+1),
 * где N — число взятых членов ряда.
 *
 *   N = 10      → ошибка < 0.19      (≈ 0.1)
 *   N = 100     → ошибка < 0.02      (≈ 0.01)
 *   N = 1 000   → ошибка < 0.002     (≈ 0.001)
 *   N = 10 000  → ошибка < 0.0002    (≈ 0.0001)
 *   N = 100 000 → ошибка < 0.00002   (≈ 0.00001)
 *   N = 1 000 000 → ошибка < 0.000002 (≈ 1e-6)
 *
 * Для получения 6 знаков после запятой (~1e-6) требуется около 500 000 итераций.
 * Этот метод неэффективен для высокоточных расчётов, но отлично подходит для
 * демонстрации численных рядов в учебных целях.
 *
 * ----------------------------------------------------------------------------
 * 4. СЛОЖНОСТЬ
 * ----------------------------------------------------------------------------
 *
 *   Время: O(iterations)
 *   Память: O(1)
 *
 * ----------------------------------------------------------------------------
 * @param iterations Количество членов ряда (iterations > 0)
 * @return Приближённое значение числа π
 *
 * @example
 * double pi = calculatePi(1000000); // ≈ 3.1415916536
 */
double calculatePi(int iterations) {
  if (iterations <= 0) {
    return 0.0;
  }

  double sum = 0.0;
  double sign = +1.0;  // +1 для нечётных индексов, -1 для чётных (начиная с i=0)
  for (int i = 0; i < iterations; ++i) {
    // Знаменатель: 2*i + 1 = 1, 3, 5, 7, ...
    double term = 1.0 / (2.0 * i + 1.0);
    sum += sign * term;
    sign = -sign;  // меняем знак для следующего члена
  }

  return 4.0 * sum;
}

/**
 * БОНУС: Вычисление π методом Монте-Карло
 *
 * ----------------------------------------------------------------------------
 * 1. ПРИНЦИП
 * ----------------------------------------------------------------------------
 * Генерирует numPoints случайных точек в квадрате [-1,1] x [-1,1]
 * и считает долю попавших в единичный круг (x² + y² ≤ 1).
 * Отношение площади круга к площади квадрата = π/4.
 *
 * ----------------------------------------------------------------------------
 * 2. АЛГОРИТМ
 * ----------------------------------------------------------------------------
 *   1. Генерируем numPoints случайных точек (x, y) с равномерным распределением.
 *   2. Подсчитываем inside — количество точек, для которых x² + y² ≤ 1.
 *   3. Возвращаем 4.0 * inside / numPoints.
 *
 * ----------------------------------------------------------------------------
 * 3. ТОЧНОСТЬ
 * ----------------------------------------------------------------------------
 * Метод Монте-Карло имеет статистическую погрешность порядка 1/√N.
 * Результат недетерминирован и зависит от генератора случайных чисел.
 *
 * Примерная точность (при использовании хорошего ГСЧ):
 *   N = 1 000       ошибка ≈ 0.03–0.05
 *   N = 10 000      ошибка ≈ 0.01–0.02
 *   N = 100 000     ошибка ≈ 0.003–0.01
 *   N = 1 000 000   ошибка ≈ 0.001–0.003
 *   N = 10 000 000  ошибка ≈ 0.0003–0.001
 *
 * Для получения 6 знаков после запятой (ошибка ~1e-6) потребуется
 * около 10^12 точек, что делает метод непригодным для высокоточных расчётов.
 * Однако метод отлично подходит для демонстрации вероятностных алгоритмов
 * и закона больших чисел.
 *
 * ----------------------------------------------------------------------------
 * 4. СЛОЖНОСТЬ
 * ----------------------------------------------------------------------------
 *   Время: O(numPoints)
 *   Память: O(1)
 *
 * ----------------------------------------------------------------------------
 * @param numPoints Количество случайных точек (numPoints > 0)
 * @return Приближённое значение π
 *
 * @example
 * double pi = calculatePiMonteCarlo(1000000); // ≈ 3.141...
 */
double calculatePiMonteCarlo(int numPoints) {
  if (numPoints <= 0) {
    return 0.0;
  }

  std::random_device rd;
  std::mt19937 gen(rd());
  std::uniform_real_distribution<double> dist(-1.0, 1.0);

  int inside = 0;
  for (int i = 0; i < numPoints; ++i) {
    double x = dist(gen);
    double y = dist(gen);
    if (x * x + y * y <= 1.0) {
      ++inside;
    }
  }
  return 4.0 * inside / numPoints;
}

